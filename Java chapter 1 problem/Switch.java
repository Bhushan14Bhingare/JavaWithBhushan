public class Switch {
    public static void main(String args[]){
        int number = 3;
        switch (number){
            case 1 : System.out.println("Samosa");
                      break;
            case 2 : System.out.println("Jalebi");
                      break;
            case 3 : System.out.println("Dinner");
                      break;
            default : System.out.println("We wake up");                              
        }
    }
}
