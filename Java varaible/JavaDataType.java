public class JavaDataType {
    public static void main(String args[]){
        short b = 90;
        char ch = 'n';// all variable content only 2 bytes
        int number = 35; // all whole number only 4 bytes or long 8 bytes
        boolean var = true; // only true and false only 1 bytes or 1 byte = 8 bites
        float price = 3.8f; // only decimal number only 4 bytes or double 8 bytes
        System.out.println(b);
        System.out.println(ch);
        System.out.println(var);
        System.out.println(number);
        System.out.println(price);
    }
    
}
